$('.gallery__item').on('click', function (e) {
    e.preventDefault();
    e.stopPropagation();
    $.fancybox({
        href: $(this).find('.gallery__item__image').attr('data-img'), helpers: {
            overlay: {
                locked: false
            }
        }
    });
});

